let activeLocale: LocaleCode = "en";

export function setLanguage(language: string | null | undefined): void {
  activeLocale = language?.toLowerCase().startsWith("ru") ? "ru" : "en";
}

export function t(key: string, values: Record<string, string | number> = {}): string {
  const template = dictionaries[activeLocale][key] ?? dictionaries.en[key] ?? key;
  return template.replace(/\{\{(\w+)\}\}/g, (_, name: string) => String(values[name] ?? ""));
}

export function translateForTest(key: string, language: string, values: Record<string, string | number> = {}): string {
  const previous = activeLocale;
  setLanguage(language);
  const translated = t(key, values);
  activeLocale = previous;
  return translated;
}
